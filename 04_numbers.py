# exponentiation in Python
print(0.11924e2)   # Output: 11.924
print(0.11924e-2)  # Output: 0.0011924

# +, -, *, /, //, %, **
print("1 + 2 =", 1 + 2)    # Output: 1 + 2 = 3
print("4 - 2 =", 4 - 2)    # Output: 4 - 2 = 2
print("2 * 3 =", 2 * 3)    # Output: 2 * 3 = 6
print("8 / 2 =", 8 / 2)    # Output: 8 / 2 = 4.0
print("7 // 3 =", 7 // 3)  # Output: 7 // 3 = 2
print("5 % 2 =", 5 % 2)    # Output: 5 % 2 = 1
print("2 ** 3 =", 2 ** 3)  # Output: 2 ** 3 = 8


# math module: sqrt() & pow()
