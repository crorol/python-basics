# 1. What is a `lambda` Function?
# -----------------------------------------------------------------------------------------------
# unlike a regular function defined with `def` Function,
# a `lambda` Function doesn't have a name.
add = lambda a, b: a + b
print(add(3, 4))   # 7

# Equivalent Regular function Definition
def add(a, b):
    return a + b



